def get_fomc_dates(start_year: int = 2008, end_year: int = 2023) -> list:
    """
    Get FOMC announcement dates within a specified year range.
    
    Args:
        start_year: Start year (inclusive), default 2008
        end_year: End year (inclusive), default 2023
        
    Returns:
        List of FOMC dates as strings in 'YYYY-MM-DD' format
    """
    return [
        date for date in FOMC_DATES_2008_2023
        if start_year <= int(date[:4]) <= end_year
    ]


